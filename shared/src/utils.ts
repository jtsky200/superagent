/**
 * Format currency amount in Swiss Francs
 */
export const formatCurrency = (amount: number, currency: string = 'CHF'): string => {
  return new Intl.NumberFormat('de-CH', {
    style: 'currency',
    currency: currency,
  }).format(amount);
};

/**
 * Format date in Swiss format
 */
export const formatDate = (date: Date | string): string => {
  const dateObj = typeof date === 'string' ? new Date(date) : date;
  return new Intl.DateTimeFormat('de-CH').format(dateObj);
};

/**
 * Format phone number in Swiss format
 */
export const formatPhoneNumber = (phone: string): string => {
  // Remove all non-digit characters
  const cleaned = phone.replace(/\D/g, '');
  
  // Swiss phone number formatting
  if (cleaned.startsWith('41')) {
    // International format
    const national = cleaned.substring(2);
    return `+41 ${national.substring(0, 2)} ${national.substring(2, 5)} ${national.substring(5, 7)} ${national.substring(7)}`;
  } else if (cleaned.startsWith('0')) {
    // National format
    return `${cleaned.substring(0, 3)} ${cleaned.substring(3, 6)} ${cleaned.substring(6, 8)} ${cleaned.substring(8)}`;
  }
  
  return phone;
};

/**
 * Validate Swiss postal code
 */
export const isValidSwissPostalCode = (postalCode: string): boolean => {
  const cleaned = postalCode.replace(/\s/g, '');
  return /^[1-9]\d{3}$/.test(cleaned);
};

/**
 * Validate Swiss UID number
 */
export const isValidSwissUID = (uid: string): boolean => {
  const pattern = /^CHE-\d{3}\.\d{3}\.\d{3}$/;
  return pattern.test(uid);
};

/**
 * Calculate age from date of birth
 */
export const calculateAge = (dateOfBirth: Date | string): number => {
  const birth = typeof dateOfBirth === 'string' ? new Date(dateOfBirth) : dateOfBirth;
  const today = new Date();
  let age = today.getFullYear() - birth.getFullYear();
  const monthDiff = today.getMonth() - birth.getMonth();
  
  if (monthDiff < 0 || (monthDiff === 0 && today.getDate() < birth.getDate())) {
    age--;
  }
  
  return age;
};

